import Foundation
import UIKit

extension UIFont {
  class var titleLightFont: UIFont {
    return UIFont.systemFont(ofSize: 22.0, weight: UIFontWeightLight)
  }

  class var buttonCommonFont: UIFont {
    return UIFont.systemFont(ofSize: 15.0, weight: UIFontWeightMedium)
  }
}
